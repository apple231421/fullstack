package a0313;

public class Operator1 {

    public static void main(String[] args) {
        // 변수 초기화
        int a = 5;
        int b = 2;

        int sum = a + b;
        int sum1 = a - b;
        int sum2 = a * b;
        int sum3 = a / b;

        System.out.println("a + b = " + sum);
        System.out.println("a - b = " + sum1);
        System.out.println("a * b = " + sum2);
        System.out.println("a / b = " + sum3);
    }
}